package com.javaprogs.javaprogs;
class SampleSwitch
{
  public static void main(String args[])
    {
      for(int i=1;i<4;i++)
       switch(i)
       {
       case 1:
         System.out.println("i is one");
         break;
       case 2:
         System.out.println("i is two");
         break;
       default:
         System.out.println(i);
        }

      }
}
