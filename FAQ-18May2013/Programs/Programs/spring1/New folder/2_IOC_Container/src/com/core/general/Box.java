package com.core.general;

public class Box {
	int length;
	int breadth;
	int height;
	User user;
	
	public Box()
	{
		length = 10;
		breadth = 10;
		height = 10;
		
	}
	public int getLength() {
		return length;
	}
	public void setLength(int length) {
		this.length = length;
	}
	public int getBreadth() {
		return breadth;
	}
	public void setBreadth(int breadth) {
		this.breadth = breadth;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public Box(int l, int b, int h)
	{ 	this.length = l;
		this.breadth = b;
		this.height = h;
		
		
	}
	
	public Box(int l, int b, int h, User user)
	{ 	this.length = l;
		this.breadth = b;
		this.height = h;
		this.user = user;
		
		
	}
	
	
	
	public String toString()
	{
		return length + ":"  + breadth + ":" + height + user.firstName;
		
		
	}
	

}
